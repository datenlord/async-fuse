//! [`Errno`]

/// Linux error code
#[derive(Debug, Clone, Copy)]
pub struct Errno(i32);

impl Errno {
    /// Returns the raw error code
    #[must_use]
    #[inline]
    pub const fn as_raw(self) -> i32 {
        self.0
    }
}

macro_rules! declare_errno{
    ($($n:ident,)+) => {
        #[allow(missing_docs)]
        impl Errno {
            $(pub const $n: Self = Self(libc::$n);)+
        }
    }
}

